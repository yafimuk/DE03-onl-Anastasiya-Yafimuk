import math

print(math.pi)  # Выводит значение числа π
print(math.sqrt(16))  # Выводит квадратный корень из 16
print(math.factorial(5))  # Выводит факториал числа 5
print(math.pow(2, 3))  # Выводит 2 в степени 3
print(math.ceil(4.2))  # Округляет число вверх
print(math.floor(4.8))  # Округляет число вниз
print(math.sin(math.pi / 2))  # Вычисляет синус π/2
print(math.cos(0))  # Вычисляет косинус 0
print(math.tan(math.pi / 4))  # Вычисляет тангенс π/4
print(math.log(100, 10))  # Вычисляет логарифм
print(math.exp(1))  # Вычисляет e в степени 1
print(math.degrees(math.pi))  # Преобразует радианы в градусы
print(math.radians(180))  # Преобразует градусы в радианы
print(math.isclose(0.1 + 0.2, 0.3))  # Проверяет, близки ли два числа
print(math.gcd(48, 18))  # Вычисляет наибольший общий делитель
print(math.lcm(4, 6))  # Вычисляет наименьшее общее кратное
print(math.comb(5, 2))  # Вычисляет число сочетаний
print(math.perm(5, 2))  # Вычисляет число перестановок
print(math.dist((1, 2), (4, 6)))  # Вычисляет расстояние между двумя точками
print(math.hypot(3, 4))  # Вычисляет длину гипотенузы
print(math.prod([1, 2, 3, 4]))  # Вычисляет произведение элементов списка