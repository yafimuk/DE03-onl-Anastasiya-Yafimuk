# ---------- Создание кортежа ----------
t = (1, 2, 2, 3, 4)
print("Исходный кортеж:", t)

# ---------- Методы кортежей ----------
count_2 = t.count(2)               # Количество вхождений элемента 2
print("count(2):", count_2)        # count(2): 2

index_3 = t.index(3)               # Индекс первого вхождения элемента 3
print("index(3):", index_3)        # index(3): 3

# ---------- Операции над кортежами ----------
# Конкатенация (объединение)
t1 = (1, 2)
t2 = (3, 4)
new_tuple = t1 + t2
print("t1 + t2:", new_tuple) 

# Повторение кортежа
repeat_tuple = t1 * 3
print("t1 * 3:", repeat_tuple)

# Индексация и срезы
print("t[0]:", t[0])
print("t[1:4]:", t[1:4])

# ---------- Проверка принадлежности ----------
print("2 in t:", 2 in t) 
print("2 in t:", 6 in t) 

# ---------- Преобразование кортежа в список ----------
# (для изменения содержимого)
lst = list(t)
lst.append(5)
t = tuple(lst)
print(t)