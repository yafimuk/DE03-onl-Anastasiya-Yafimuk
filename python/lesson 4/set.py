# ---------- Создание множества ----------
s = {1, 2, 3}
print("Исходное множество:", s)

s2 = set([3, 4, 5])

# ---------- Размер и проверка принадлежности ----------
print("len(s):", len(s))
print("2 in s:", 2 in s)
print("10 in s:", 10 in s)

# ---------- Сравнение множеств ----------
print("s.isdisjoint({4,5}):", s.isdisjoint({4, 5}))  # True (нет общих элементов)

# ---------- Добавление и удаление элементов ----------
s.add(6)  # Добавляет элемент
print("add(6):", s)

s.remove(2)  # Удаляет элемент
print("remove(2):", s)

# ---------- Объединение и пересечения ----------
print("union:", s.union(s2))  # Объединение множеств → {1, 3, 4, 5, 6}
print("intersection:", s.intersection(s2))  # Пересечение множеств → {3}
print("difference:", s.difference(s2))  # Разность → {1, 6}
